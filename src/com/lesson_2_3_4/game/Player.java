package com.lesson_2_3_4.game;//1

public class Player {	
	private String name;
	private int number;

	public Player(String name) {
		this.name = name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setNumber(int number) {
		this.number = number;
	}
	public int getNumber() {
		return number;
	}
	
}	
